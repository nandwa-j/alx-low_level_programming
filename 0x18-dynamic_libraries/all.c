
#include <stdio.h>
/*
 * add - adds two integers
 * @a: integer
 * @b: integer
 * Return: addition
 */
int add(int a, int b)
{
	return (a + b);
}
/*
 * mul - multiplies two integers
 * @a: integer
 * @b: integer
 * Return: multiplication
 */
int mul(int a, int b)
{       
        return (a * b);
}
/*
 * mod - gets the remainder two integers
 * @a: integer
 * @b: integer
 * Return: remainder
 */
int mod(int a, int b)
{       
        return (a % b);
}
/*
 * sub - subtracts two integers
 * @a: integer
 * @b: integer
 * Return: subraction
 */
int sub(int a, int b)
{       
        return (a - b);
}

/*
 * div - divides two integers
 * @a: integer
 * @b: integer
 * Return: quotient
 */
int div(int a, int b)
{       
        return (a / b);
}
