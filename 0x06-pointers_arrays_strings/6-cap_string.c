char *cap_string(char *s)
{
    int i = 0;

   
    if (s[i] >= 'a' && s[i] <= 'z') {
        s[i] -= 32;
    }

   
    for (i = 1; s[i] != '\0'; i++) {
        
        if (s[i] >= 'a' && s[i] <= 'z') {
            
            if (s[i - 1] == ' ' || s[i - 1] == '\t' || s[i - 1] == '\n' ||
                s[i - 1] == ',' || s[i - 1] == ';' || s[i - 1] == '.' ||
                s[i - 1] == '!' || s[i - 1] == '?' || s[i - 1] == '"' ||
                s[i - 1] == '(' || s[i - 1] == ')' || s[i - 1] == '{' || s[i - 1] == '}' ||
                s[i - 1] == '\0') {
                s[i] -= 32; 
            }
        }
    }
    return s;
}
